    <h2 class="section-title">How does this work?</h2>
    <div class="section-block">
        <p>This is a highly configurable PHP library that enables you to produce
            chart-like images. </p>
        <p>You simply create a new object of the chart class, set the dimensions
            and the chart dataset (and all the other available configurations,
            if needed) and a chart image is created. You can then display the image
            on the page or store it on a file.</p>
        <p>Behind the curtain, the library calculates the dimensions of all the
            components (graph, axis, title, labels, columns / bars, lines / pie
            slices, etc..) and assembles the final image.</p>
    </div>
